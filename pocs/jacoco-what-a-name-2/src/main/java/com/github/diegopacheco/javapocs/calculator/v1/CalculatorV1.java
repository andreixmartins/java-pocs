package com.github.diegopacheco.javapocs.calculator.v1;

public class CalculatorV1 {

    public int sum(int a, int b){
        return a + b;
    }

    public int sub(int a, int b){
        return a - b;
    }

    public int mult(int a, int b){
        return a * b;
    }

    public int div(int a, int b){
        return a / b;
    }

}
